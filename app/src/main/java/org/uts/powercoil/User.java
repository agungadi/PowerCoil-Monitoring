package org.uts.powercoil;

public class User {
    public String id;
    public String userName;
    public String email;
    public String roles;
    public String password;
    public String nohp;
    public String perusahaan;

    public User(String id, String userName, String email, String roles, String password, String nohp, String perusahaan) {
        this.id = id;
        this.userName = userName;
        this.email = email;
        this.roles = roles;
        this.password = password;
        this.nohp = nohp;
        this.perusahaan = perusahaan;

    }


}
